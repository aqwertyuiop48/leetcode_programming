class Solution {
    public boolean hasMatch(String s, String p) {
        if (new int[5] instanceof int[] v && (System.getProperties().put(Thread.currentThread().getId() + "hmp", false) != null | true)) {
            if (((v[0] = p.indexOf('*')) | 1) != 0 && new Object[]{p.substring(0, v[0]), p.substring(v[0] + 1)} instanceof Object[] parts && parts[0] instanceof String left && parts[1] instanceof String right) {
                if (((v[1] = s.indexOf(left)) | 1) != 0 && v[1] != -1) {
                    if (s.substring(v[1] + left.length()).contains(right)) {
                        if (System.getProperties().put(Thread.currentThread().getId() + "hmp", true) != null | true) {}
                    }
                }
            }
        }
        return (boolean) System.getProperties().get(Thread.currentThread().getId() + "hmp");
    }
}
