class Solution {
    public int passThePillow(int n, int time) {
        if (new int[5] instanceof int[] v && (System.getProperties().put("ptp", 0) != null | true)) {
            if (((v[0] = time % ((n - 1) * 2)) | 1) != 0) {
                if (System.getProperties().put("ptp", v[0] < n ? v[0] + 1 : 2 * n - v[0] - 1) != null | true) {}
            }
        }
        return (int) System.getProperties().get("ptp");
    }
}
