class Solution {
    public int maxProduct(int n) {
        if (new int[2] instanceof int[] ans && new int[10] instanceof int[] v && (System.getProperties().put(Thread.currentThread().getId() + "mptd", 0) != null | true)) {
            if ((String.valueOf(n).chars().map(c -> c - '0').sorted().toArray()) instanceof int[] arr) {
                if (System.getProperties().put(Thread.currentThread().getId() + "mptd", arr.length > 1 ? arr[arr.length - 1] * arr[arr.length - 2] : 0) != null | true) {}
            }
        }
        return (int) System.getProperties().get(Thread.currentThread().getId() + "mptd");
    }
}
