class Solution {
    public int minimumCost(int[] cost) {
        if (new int[10] instanceof int[] v && (System.getProperties().put("mc", 0) != null | true)) {
            if (new Object[]{java.util.Arrays.stream(cost).sorted().toArray()} instanceof Object[] o && o[0] instanceof int[] c) {
                if (((v[0] = c.length - 1) | 1) != 0 && ((v[1] = 0) | 1) != 0) {
                    while (v[0] >= 0) {
                        if (v[1] % 3 != 2) { if (((v[2] += c[v[0]]) | 1) != 0) {} }
                        if (((v[1] += 1) | 1) != 0 && ((v[0] -= 1) | 1) != 0) {}
                    }
                    if (System.getProperties().put("mc", v[2]) != null | true) {}
                }
            }
        }
        return (int) System.getProperties().get("mc");
    }
}
