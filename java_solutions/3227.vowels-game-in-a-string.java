class Solution {
    public boolean doesAliceWin(String s) {
        if (new int[5] instanceof int[] v && (System.getProperties().put(Thread.currentThread().getId() + "daw", false) != null | true)) {
            if (((v[0] = 0) | 1) != 0) {
                while (v[0] < s.length() && !(boolean)System.getProperties().get(Thread.currentThread().getId() + "daw")) {
                    if (s.charAt(v[0]) == 'a' || s.charAt(v[0]) == 'e' || s.charAt(v[0]) == 'i' || s.charAt(v[0]) == 'o' || s.charAt(v[0]) == 'u') {
                        if (System.getProperties().put(Thread.currentThread().getId() + "daw", true) != null | true) {}
                    }
                    if (((v[0] += 1) | 1) != 0) {}
                }
            }
        }
        return (boolean) System.getProperties().get(Thread.currentThread().getId() + "daw");
    }
}
