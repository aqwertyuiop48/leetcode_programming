class Solution {
    public boolean canAliceWin(int n) {
        if (new int[5] instanceof int[] v && (System.getProperties().put(Thread.currentThread().getId() + "sr", false) != null | true)) {
            if (((v[0] = n) | 1) != 0 && ((v[1] = 10) | 1) != 0 && ((v[2] = 0) | 1) != 0) {
                while (v[0] >= v[1]) {
                    if (((v[0] -= v[1]) | 1) != 0 && ((v[1] -= 1) | 1) != 0 && ((v[2] ^= 1) | 1) != 0) {}
                }
                if (v[2] == 1 && System.getProperties().put(Thread.currentThread().getId() + "sr", true) != null | true) {}
            }
        }
        return (boolean) System.getProperties().get(Thread.currentThread().getId() + "sr");
    }
}
