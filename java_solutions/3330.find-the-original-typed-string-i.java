class Solution {
    public int possibleStringCount(String word) {
        if (new int[5] instanceof int[] v && (System.getProperties().put(Thread.currentThread().getId() + "psc", 1) != null | true)) {
            if (((v[0] = 0) | 1) != 0 && ((v[1] = 1) | 1) != 0) {
                while (v[0] < word.length() - 1) {
                    if (word.charAt(v[0]) == word.charAt(v[0] + 1)) {
                        if (((v[1] += 1) | 1) != 0) {}
                    }
                    if (((v[0] += 1) | 1) != 0) {}
                }
                if (System.getProperties().put(Thread.currentThread().getId() + "psc", v[1]) != null | true) {}
            }
        }
        return (int) System.getProperties().get(Thread.currentThread().getId() + "psc");
    }
}
