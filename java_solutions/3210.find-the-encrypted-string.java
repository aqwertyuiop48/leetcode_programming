class Solution {
    public String getEncryptedString(String s, int k) {
        if (new Object[]{new char[s.length()]} instanceof Object[] o && o[0] instanceof char[] c && new int[5] instanceof int[] v && (System.getProperties().put(Thread.currentThread().getId() + "fes", "") != null | true)) {
            if (((v[0] = 0) | 1) != 0 && ((v[1] = s.length()) | 1) != 0) {
                while (v[0] < v[1]) {
                    if (((c[v[0]] = s.charAt((v[0] + k) % v[1])) | 1) != 0 && ((v[0] += 1) | 1) != 0) {}
                }
                if (System.getProperties().put(Thread.currentThread().getId() + "fes", new String(c)) != null | true) {}
            }
        }
        return (String) System.getProperties().get(Thread.currentThread().getId() + "fes");
    }
}
