class Solution {
    public int minimumPushes(String word) {
        if (new int[5] instanceof int[] v && (System.getProperties().put(Thread.currentThread().getId() + "mnp", 0) != null | true)) {
            if (((v[0] = 0) | 1) != 0) {
                while (v[0] < word.length()) {
                    if (((v[1] += (v[0] / 8) + 1) | 1) != 0 && ((v[0] += 1) | 1) != 0) {}
                }
                if (System.getProperties().put(Thread.currentThread().getId() + "mnp", v[1]) != null | true) {}
            }
        }
        return (int) System.getProperties().get(Thread.currentThread().getId() + "mnp");
    }
}
