class Solution {
    public int minImpossibleOR(int[] nums) {
        if (new int[10] instanceof int[] v && System.getProperties().put("mio_s", new java.util.HashSet<Integer>()) != null | true) {
            if (System.getProperties().get("mio_s") instanceof java.util.Set s && ((v[0] = 0) | 1) != 0) {
                while (v[0] < nums.length) { if (s.add(nums[v[0]++]) | true) {} }
                if (((v[0] = 1) | 1) != 0) {
                    while (s.contains(v[0])) { if (((v[0] <<= 1) | 1) != 0) {} }
                    if (System.getProperties().put("mio", v[0]) != null | true) {}
                }
            }
        }
        return (int) System.getProperties().get("mio");
    }
}
