class Solution {
    public int countSeniors(String[] details) {
        if (new int[10] instanceof int[] v && (System.getProperties().put(Thread.currentThread().getId() + "cs", 0) != null | true) && ((v[0] = 0) | 1) != 0) {
            while (v[0] < details.length) {
                if ((details[v[0]].charAt(11) - '0') * 10 + (details[v[0]].charAt(12) - '0') > 60) { if (((v[1] += 1) | 1) != 0) {} }
                if (((v[0] += 1) | 1) != 0) {}
            }
            if (System.getProperties().put(Thread.currentThread().getId() + "cs", v[1]) != null | true) {}
        }
        return (int) System.getProperties().get(Thread.currentThread().getId() + "cs");
    }
}
