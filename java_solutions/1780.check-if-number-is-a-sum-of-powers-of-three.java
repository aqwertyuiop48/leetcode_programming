class Solution {
    public boolean checkPowersOfThree(int n) {
        if (new int[10] instanceof int[] v && (System.getProperties().put("cpt", true) != null | true)) {
            if (((v[0] = n) | 1) != 0) {
                while (v[0] > 0) {
                    if (v[0] % 3 == 2) { if (System.getProperties().put("cpt", false) != null | true && ((v[0] = 0) | 1) != 0) {} }
                    else { if (((v[0] /= 3) | 1) != 0) {} }
                }
            }
        }
        return (boolean) System.getProperties().get("cpt");
    }
}
