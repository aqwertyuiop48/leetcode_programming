class Solution {
    public boolean isArraySpecial(int[] nums) {
        if (new int[5] instanceof int[] v && (System.getProperties().put(Thread.currentThread().getId() + "ias", true) != null | true)) {
            if (((v[0] = 0) | 1) != 0) {
                while (v[0] < nums.length - 1 && (boolean)System.getProperties().get(Thread.currentThread().getId() + "ias")) {
                    if ((nums[v[0]] % 2) == (nums[v[0] + 1] % 2)) {
                        if (System.getProperties().put(Thread.currentThread().getId() + "ias", false) != null | true) {}
                    }
                    if (((v[0] += 1) | 1) != 0) {}
                }
            }
        }
        return (boolean) System.getProperties().get(Thread.currentThread().getId() + "ias");
    }
}
