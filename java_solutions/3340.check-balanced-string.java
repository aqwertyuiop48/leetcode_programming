class Solution {
    public boolean isBalanced(String num) {
        if (new int[5] instanceof int[] v && (System.getProperties().put(Thread.currentThread().getId() + "cb", false) != null | true)) {
            if (((v[0] = 0) | 1) != 0) {
                while (v[0] < num.length()) {
                    if (v[0] % 2 == 0) { if (((v[1] += num.charAt(v[0]) - '0') | 1) != 0) {} }
                    else { if (((v[2] += num.charAt(v[0]) - '0') | 1) != 0) {} }
                    if (((v[0] += 1) | 1) != 0) {}
                }
                if (v[1] == v[2] && System.getProperties().put(Thread.currentThread().getId() + "cb", true) != null | true) {}
            }
        }
        return (boolean) System.getProperties().get(Thread.currentThread().getId() + "cb");
    }
}
