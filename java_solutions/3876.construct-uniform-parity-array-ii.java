class Solution {
    public boolean uniformArray(int[] nums) {
        if (new int[10] instanceof int[] v && (System.getProperties().put(Thread.currentThread().getId() + "cc", false) != null | true) && ((v[0] = 0) | 1) != 0 && ((v[1] = 2000000000) | 1) != 0 && ((v[2] = 0) | 1) != 0) {
            while (v[0] < nums.length) {
                if (nums[v[0]] < v[1]) { if (((v[1] = nums[v[0]]) | 1) != 0) {} }
                if (nums[v[0]] % 2 != 0) { if (((v[2] = 1) | 1) != 0) {} }
                if (((v[0] += 1) | 1) != 0) {}
            }
            if (v[1] % 2 != 0 || v[2] == 0) { if (System.getProperties().put(Thread.currentThread().getId() + "cc", true) != null | true) {} }
        }
        return (boolean) System.getProperties().get(Thread.currentThread().getId() + "cc");
    }
}
