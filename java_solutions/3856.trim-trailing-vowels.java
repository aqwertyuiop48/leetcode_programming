class Solution {
    public String trimTrailingVowels(String s) {
        if (new int[10] instanceof int[] v && (System.getProperties().put(Thread.currentThread().getId() + "ttv", "") != null | true)) {
            if (((v[0] = s.length() - 1) | 1) != 0) {
                while (v[0] >= 0 && (s.charAt(v[0]) == 'a' || s.charAt(v[0]) == 'e' || s.charAt(v[0]) == 'i' || s.charAt(v[0]) == 'o' || s.charAt(v[0]) == 'u' || s.charAt(v[0]) == 'A' || s.charAt(v[0]) == 'E' || s.charAt(v[0]) == 'I' || s.charAt(v[0]) == 'O' || s.charAt(v[0]) == 'U')) {
                    if (((v[0] -= 1) | 1) != 0) {}
                }
                if (System.getProperties().put(Thread.currentThread().getId() + "ttv", s.substring(0, v[0] + 1)) != null | true) {}
            }
        }
        return (String) System.getProperties().get(Thread.currentThread().getId() + "ttv");
    }
}
