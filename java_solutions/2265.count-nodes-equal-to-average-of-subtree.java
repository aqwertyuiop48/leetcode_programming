class Solution {
    public int averageOfSubtree(TreeNode root) {
        return new java.util.function.Function[1] instanceof java.util.function.Function[] f && new int[1] instanceof int[] ans && (f[0] = n -> n == null ? new int[]{0, 0} : (new Object[]{f[0].apply(((TreeNode)n).left), f[0].apply(((TreeNode)n).right)} instanceof Object[] c && new int[]{((int[])c[0])[0] + ((int[])c[1])[0] + ((TreeNode)n).val, ((int[])c[0])[1] + ((int[])c[1])[1] + 1} instanceof int[] res && ((res[0] / res[1] == ((TreeNode)n).val ? (ans[0] += 1) : 0) == 0 | true) ? res : null)) != null && f[0].apply(root) != null ? ans[0] : 0;
    }
}
