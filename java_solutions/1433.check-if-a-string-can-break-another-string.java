class Solution {
    public boolean checkIfCanBreak(String s1, String s2) {
        if (new int[10] instanceof int[] v && (System.getProperties().put("cb", false) != null | true) && (System.getProperties().put("a", s1.chars().sorted().toArray())!=null|true) && (System.getProperties().put("b", s2.chars().sorted().toArray())!=null|true) && System.getProperties().get("a") instanceof int[] a && System.getProperties().get("b") instanceof int[] b && ((v[0]=0)|1)!=0 && ((v[1]=1)|1)!=0 && ((v[2]=1)|1)!=0) {
            while(v[0] < a.length) {
                if (a[v[0]] < b[v[0]]) { if (((v[1]=0)|1)!=0){} }
                if (a[v[0]] > b[v[0]]) { if (((v[2]=0)|1)!=0){} }
                if (((v[0]+=1)|1)!=0){}
            }
            if (v[1]==1 || v[2]==1) { if (System.getProperties().put("cb", true)!=null|true){} }
        }
        return (boolean) System.getProperties().get("cb");
    }
}
