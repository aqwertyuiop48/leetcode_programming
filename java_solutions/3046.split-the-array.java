class Solution {
    public boolean isPossibleToSplit(int[] nums) {
        if (new int[105] instanceof int[] cnt && new int[5] instanceof int[] v && (System.getProperties().put(Thread.currentThread().getId() + "ipts", true) != null | true)) {
            if (((v[0] = 0) | 1) != 0) {
                while (v[0] < nums.length && (boolean)System.getProperties().get(Thread.currentThread().getId() + "ipts")) {
                    if (((cnt[nums[v[0]]] += 1) | 1) != 0 && cnt[nums[v[0]]] > 2) {
                        if (System.getProperties().put(Thread.currentThread().getId() + "ipts", false) != null | true) {}
                    }
                    if (((v[0] += 1) | 1) != 0) {}
                }
            }
        }
        return (boolean) System.getProperties().get(Thread.currentThread().getId() + "ipts");
    }
}
