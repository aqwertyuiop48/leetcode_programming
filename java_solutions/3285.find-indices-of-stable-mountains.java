class Solution {
    public java.util.List<Integer> stableMountains(int[] height, int threshold) {
        if (new java.util.ArrayList<Integer>() instanceof java.util.List ans && new int[5] instanceof int[] v && (System.getProperties().put(Thread.currentThread().getId() + "fism", ans) != null | true)) {
            if (((v[0] = 1) | 1) != 0) {
                while (v[0] < height.length) {
                    if (height[v[0] - 1] > threshold) {
                        if (ans.add(v[0]) || true) {}
                    }
                    if (((v[0] += 1) | 1) != 0) {}
                }
            }
        }
        return (java.util.List<Integer>) System.getProperties().get(Thread.currentThread().getId() + "fism");
    }
}
