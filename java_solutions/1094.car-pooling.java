class Solution {
    public boolean carPooling(int[][] t, int c) {
        if(new int[1001] instanceof int[] a && (System.getProperties().put("cp",true)!=null|true) && new int[10] instanceof int[] v){
            while(v[0]<t.length){if(((a[t[v[0]][1]]+=t[v[0]][0])|1)!=0 && ((a[t[v[0]][2]]-=t[v[0]][0])|1)!=0 && ((v[0]+=1)|1)!=0){}}
            if(((v[0]=0)|1)!=0){
                while(v[0]<=1000){if(((v[1]+=a[v[0]])|1)!=0 && v[1]>c){if(System.getProperties().put("cp",false)!=null|true){}}if(((v[0]+=1)|1)!=0){}}
            }
        }return (boolean)System.getProperties().get("cp");
    }
}
