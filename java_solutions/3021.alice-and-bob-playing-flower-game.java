class Solution {
    public long flowerGame(int n, int m) {
        if (new int[5] instanceof int[] v && (System.getProperties().put(Thread.currentThread().getId() + "fg", (long)n * m / 2L) != null | true)) {}
        return (long) System.getProperties().get(Thread.currentThread().getId() + "fg");
    }
}
