class Solution {
    public int maxJump(int[] stones) {
        if (new int[10] instanceof int[] v && (System.getProperties().put("fj", stones[1] - stones[0]) != null | true)) {
            if (((v[0] = 2) | 1) != 0) {
                while (v[0] < stones.length) {
                    if (stones[v[0]] - stones[v[0] - 2] > (int)System.getProperties().get("fj")) { if (System.getProperties().put("fj", stones[v[0]] - stones[v[0] - 2]) != null | true) {} }
                    if (((v[0] += 1) | 1) != 0) {}
                }
            }
        }
        return (int) System.getProperties().get("fj");
    }
}
