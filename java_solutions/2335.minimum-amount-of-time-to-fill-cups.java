class Solution {
    public int fillCups(int[] amount) {
        if (new int[10] instanceof int[] v && (System.getProperties().put("fc", 0) != null | true)) {
            if (((v[0] = Math.max(amount[0], Math.max(amount[1], amount[2]))) | 1) != 0 && ((v[1] = (amount[0] + amount[1] + amount[2] + 1) / 2) | 1) != 0) {
                if (System.getProperties().put("fc", Math.max(v[0], v[1])) != null | true) {}
            }
        }
        return (int) System.getProperties().get("fc");
    }
}
