class Solution {
    public int numDupDigitsAtMostN(int N) {
        if (new int[]{0, N + 1, 0, 0, 0, 0, 0, 0, 0, 0} instanceof int[] v && new int[10] instanceof int[] L) {
            while (v[0] < 5) {
                if (v[0] == 0) {
                    if (v[1] > 0) {
                        if (((L[v[2]] = v[1] % 10) | 1) != 0 && ((v[1] /= 10) | 1) != 0) {
                            if (((v[2] += 1) | 1) != 0) {}
                        }
                    } else {
                        if (((v[3] = 0) | 1) != 0 && ((v[4] = v[2] - 1) | 1) != 0 && ((v[0] = 1) | 1) != 0) {}
                    }
                } else if (v[0] == 1) {
                    if (v[3] < v[4]) {
                        if (((v[5] = L[v[3]]) | 1) != 0 && ((L[v[3]] = L[v[4]]) | 1) != 0 && ((L[v[4]] = v[5]) | 1) != 0) {
                            if (((v[3] += 1) | 1) != 0 && ((v[4] -= 1) | 1) != 0) {}
                        }
                    } else {
                        if (((v[3] = 1) | 1) != 0 && ((v[0] = 2) | 1) != 0) {}
                    }
                } else if (v[0] == 2) {
                    if (v[3] < v[2]) {
                        if (((v[5] = 1) | 1) != 0 && ((v[4] = 0) | 1) != 0) {
                            while (v[4] < v[3] - 1) {
                                if (((v[5] *= (9 - v[4])) | 1) != 0 && ((v[4] += 1) | 1) != 0) {}
                            }
                        }
                        if (((v[6] += 9 * v[5]) | 1) != 0 && ((v[3] += 1) | 1) != 0) {}
                    } else {
                        if (((v[3] = 0) | 1) != 0 && ((v[8] = 0) | 1) != 0 && ((v[0] = 3) | 1) != 0) {}
                    }
                } else if (v[0] == 3) {
                    if (v[3] < v[2]) {
                        if (((v[4] = (v[3] > 0 ? 0 : 1)) | 1) != 0) {
                            while (v[4] < L[v[3]]) {
                                if ((v[8] & (1 << v[4])) == 0) {
                                    if (((v[5] = 1) | 1) != 0 && ((v[7] = 0) | 1) != 0) {
                                        while (v[7] < v[2] - v[3] - 1) {
                                            if (((v[5] *= (9 - v[3] - v[7])) | 1) != 0 && ((v[7] += 1) | 1) != 0) {}
                                        }
                                    }
                                    if (((v[6] += v[5]) | 1) != 0) {}
                                }
                                if (((v[4] += 1) | 1) != 0) {}
                            }
                        }
                        if ((v[8] & (1 << L[v[3]])) != 0) {
                            if (((v[0] = 4) | 1) != 0) {}
                        } else {
                            if (((v[8] |= (1 << L[v[3]])) | 1) != 0 && ((v[3] += 1) | 1) != 0) {}
                        }
                    } else {
                        if (((v[0] = 4) | 1) != 0) {}
                    }
                } else if (v[0] == 4) {
                    if (((N = N - v[6]) | 1) != 0 && ((v[0] = 5) | 1) != 0) {}
                }
            }
        }
        return N;
    }
}
